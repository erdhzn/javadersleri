package Gun16;

public class JavaForLoop2 {
    // 1 den 10 a kadar sayilari yazdiriniz
    // 10 dan 0 a kadar sayilari ayzdirin

    public static void main(String[] args) {
        for (int i=0;i<10; i++){
            System.out.println("i_i = " + i);
        }
        for (int i=10; i>=0;i--){
            System.out.println("g_i = " + i);;
        }
    }
}
